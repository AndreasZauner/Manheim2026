create extension if not exists pgcrypto;

create table if not exists public.profiles (
  id uuid primary key references auth.users(id) on delete cascade,
  email text,
  full_name text,
  role text not null default 'viewer'
    check (role in ('admin','professor','technical_lead','assistant','trench_lead','participant','viewer')),
  is_active boolean not null default false,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

create table if not exists public.tasks (
  id bigint generated by default as identity primary key,
  title text not null,
  description text,
  category text not null,
  subcategory text,
  due_date date,
  status text not null default 'offen'
    check (status in ('offen','laufend','erledigt','blockiert')),
  priority text not null default 'mittel'
    check (priority in ('hoch','mittel','niedrig')),
  assigned_role text,
  created_by uuid references auth.users(id) on delete set null,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now(),
  constraint tasks_title_unique unique (title)
);

create table if not exists public.notes (
  id bigint generated by default as identity primary key,
  title text not null,
  body text,
  category text not null,
  subcategory text,
  note_type text not null default 'note'
    check (note_type in ('note','idea','decision')),
  status text not null default 'offen',
  created_by uuid references auth.users(id) on delete set null,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now(),
  constraint notes_title_unique unique (title)
);

-- Older setups used a fixed category check. Categories are now editable in the app,
-- so existing databases should only require a non-empty value.
alter table public.notes drop constraint if exists notes_category_check;
alter table public.notes drop constraint if exists notes_category_not_blank;
alter table public.notes
  add constraint notes_category_not_blank check (length(trim(category)) > 0);

create table if not exists public.participants (
  id bigint generated by default as identity primary key,
  full_name text not null,
  public_role text,
  availability_from date,
  availability_to date,
  status text not null default 'zu_klären'
    check (status in ('gesetzt','zugesagt','zu_klären','anzufragen')),
  availability_note text,
  source_note text,
  created_by uuid references auth.users(id) on delete set null,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now(),
  constraint participants_full_name_unique unique (full_name)
);

create table if not exists public.participant_private (
  participant_id bigint primary key references public.participants(id) on delete cascade,
  phone text,
  email text,
  internal_note text,
  updated_at timestamptz not null default now()
);

create or replace function public.touch_updated_at()
returns trigger
language plpgsql
as $$
begin
  new.updated_at = now();
  return new;
end;
$$;

